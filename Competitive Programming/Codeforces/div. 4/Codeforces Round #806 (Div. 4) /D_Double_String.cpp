void solve()
{
	int n; cin >> n;
	vector <string> vs(n);
	map <string, bool> m;
	for (string &s: vs) {
		cin >> s;
		m[s] = 1;
	}
	for (string s: vs) {
		int ok = 0;
		for (int l = 1; l <= s.length(); l++) {
			if (m[s.substr(0, l)] && m[s.substr(l, s.length() - l)]) {
				ok = 1;
				break;
			}
		}
		cout << ok;
	}
	cout << endl;
}

